let respuestasCorrectas = 0;
let respuestasIncorrectas = 0;
let preguntasRestantes = [];
let totalPreguntas = 6;
let preguntasMostradas = 0;



function comenzarJuegoMundial() {
    
  respuestasCorrectas = 0;
  respuestasIncorrectas = 0;
  preguntasMostradas = 0;
  
  preguntasRestantes = [...preguntasFutbolMundial];
  preguntasRestantes = mezclarArray(preguntasRestantes);
  
  document.getElementById("pantalla-inicial").style.display = "none";
  document.getElementById("pantalla-juego-mundial").style.display = "block";
  cargarPregunta();
}

function mezclarArray(array) {
  // Algoritmo de Fisher-Yates
  for (let i = array.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [array[i], array[j]] = [array[j], array[i]];
  }
  return array;
}


function cargarPregunta() {
  if (preguntasMostradas >= totalPreguntas || preguntasRestantes.length === 0) {
    terminarJuego();
    return;
  }
  // Tomamos la siguiente pregunta de la lista mezclada
  const preguntaActual = preguntasRestantes.shift();
  preguntasMostradas++;

  // Mostramos la pregunta y las opciones
  document.getElementById("pregunta").innerText = preguntaActual.pregunta;
  const opciones = document.getElementById("opciones");
  opciones.innerHTML = ""; // Limpiamos las opciones anteriores

  preguntaActual.opciones.forEach((opcion, index) => {
    const opcionDiv = document.createElement("div");
    opcionDiv.className = "opcion";
    opcionDiv.innerText = opcion;
    opcionDiv.onclick = () => comprobarRespuesta(opcion, preguntaActual.respuestaCorrecta);
    opciones.appendChild(opcionDiv);
  });
}


function comprobarRespuesta(opElegida, respuestaCorrecta) {
  if (opElegida === respuestaCorrecta) { 
    respuestasCorrectas++;
    // alert("¡Respuesta correcta!");
  } else { 
    respuestasIncorrectas++;
    // alert("Respuesta incorrecta. La respuesta correcta era: " + respuestaCorrecta);
  }
  cargarPregunta();
}


function terminarJuego(){
  
  document.getElementById("pantalla-juego-mundial").style.display = "none";
 
  document.getElementById("pantalla-final").style.display = "block";
 
  document.getElementById("cantidadAcertadas").innerHTML = `Respuestas correctas: ${respuestasCorrectas}<br>Respuestas incorrectas: ${respuestasIncorrectas}`;
}












